"""
Pack 34.1 — нормализация поля degree (степень/квалификация) в applicant.education.

Решает две задачи:

1. OCR диплома возвращает английский degree ('bachelor', 'specialist', 'master',
   'phd', 'secondary') — переводим в русские эквиваленты которые ждут UI селект
   и шаблоны CV: «Бакалавр», «Специалист», «Магистр», «Кандидат наук»,
   «Среднее специальное».

2. Для инженерных специальностей (08.xx, 11.xx-29.xx по классификатору ОКСО
   2024 — группа «Инженерное дело, технологии и технические науки») вместо
   юридической «Специалист» возвращаем привычную квалификацию «Инженер»,
   которая в большинстве российских дипломов фактически и стоит.

Используется в:
- backend/app/api/client_documents_admin.py (_build_education_from_diploma)
- backend/app/api/client_portal.py (_build_education_from_diploma)
- backend/app/api/import_package.py (build_education_from_diploma)
- backend/app/services/university_generator.py (LEVEL_NAMES override)
"""
from __future__ import annotations

from typing import Optional
import re


# OCR результат → русский эквивалент для UI и шаблонов CV
DEGREE_EN_TO_RU = {
    "bachelor": "Бакалавр",
    "specialist": "Специалист",
    "master": "Магистр",
    "phd": "Кандидат наук",
    "secondary": "Среднее специальное",
}


# Префиксы кодов ОКСО 2024 для инженерных специальностей.
# Группа «Инженерное дело, технологии и технические науки» — коды 08-29
# (полная группа по официальной номенклатуре Минобрнауки).
# Также включаем 07.xx «Архитектура» — на дипломах архитектора часто
# тоже стоит «инженер-архитектор».
ENGINEERING_OKSO_PREFIXES = {
    "07",  # Архитектура
    "08",  # Техника и технологии строительства
    "09",  # Информатика и вычислительная техника (для старых "инженер-программист")
    "10",  # Информационная безопасность
    "11",  # Электроника, радиотехника и системы связи
    "12",  # Фотоника, приборостроение, оптические и биотехнические системы
    "13",  # Электро- и теплоэнергетика
    "14",  # Ядерная энергетика и технологии
    "15",  # Машиностроение
    "16",  # Физико-технические науки и технологии
    "17",  # Оружие и системы вооружения
    "18",  # Химические технологии
    "19",  # Промышленная экология и биотехнологии
    "20",  # Техносферная безопасность и природообустройство
    "21",  # Прикладная геология, горное дело, нефтегазовое дело и геодезия
    "22",  # Технологии материалов
    "23",  # Техника и технологии наземного транспорта
    "24",  # Авиационная и ракетно-космическая техника
    "25",  # Аэронавигация и эксплуатация авиационной и ракетно-космической техники
    "26",  # Техника и технологии кораблестроения и водного транспорта
    "27",  # Управление в технических системах
    "28",  # Нанотехнологии и наноматериалы
    "29",  # Технологии лёгкой промышленности
}


_CODE_RE = re.compile(r"^(\d{2})\.")


def is_engineering_specialty_code(code: Optional[str]) -> bool:
    """
    True если specialty.code — инженерная (по prefix ОКСО).

    Примеры:
        "08.03.01" → True (строительство)
        "09.03.04" → True (программная инженерия)
        "38.03.01" → False (экономика)
        "40.03.01" → False (юриспруденция)
        None / "" / "abc" → False
    """
    if not code:
        return False
    m = _CODE_RE.match(code.strip())
    if not m:
        return False
    return m.group(1) in ENGINEERING_OKSO_PREFIXES


def normalize_degree(
    degree: Optional[str],
    specialty_code: Optional[str] = None,
) -> Optional[str]:
    """
    Нормализует поле degree:

    1. Если degree пустое → возвращает как есть (None/пустую строку).
    2. Если degree английское ('bachelor', 'specialist', 'master', 'phd',
       'secondary') → конвертит в русское ('Бакалавр' и т.д.).
    3. Если после конвертации degree == 'Специалист' И specialty_code
       инженерный → возвращает 'Инженер' (как обычно пишут в российских
       инженерных дипломах).
    4. Иначе возвращает как есть (уже валидное русское или unknown).

    Args:
        degree: значение из OCR или БД. EN или RU.
        specialty_code: код специальности (e.g. "08.03.01") для определения
                        инженерного направления. Опционально.
    """
    if not degree:
        return degree

    # Шаг 1-2: EN → RU
    normalized = DEGREE_EN_TO_RU.get(degree.strip().lower(), degree)

    # Шаг 3: Специалист + инженерный код → Инженер
    if normalized == "Специалист" and is_engineering_specialty_code(specialty_code):
        return "Инженер"

    return normalized


def extract_specialty_code(specialty_text: Optional[str]) -> Optional[str]:
    """
    Извлекает код ОКСО из строки specialty.

    Примеры:
        "08.03.01 Строительство" → "08.03.01"
        "Информатика" → None
        None → None
    """
    if not specialty_text:
        return None
    m = re.match(r"^\s*(\d{2}\.\d{2}\.\d{2})", specialty_text.strip())
    return m.group(1) if m else None
