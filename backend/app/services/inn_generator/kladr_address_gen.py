"""
Pack 17.1 — Генератор реалистичных адресов для 10 базовых регионов.

Используется когда у заявителя нет своего адреса регистрации, или когда
менеджер хочет «омолодить» данные клиента в анкете.

ПОДХОД: захардкоженный массив реальных улиц для 10 ключевых регионов.
Случайно выбираем улицу + номер дома (1-100) + номер квартиры (1-150).
Адрес выглядит реально потому что улицы существуют, но привязки к
конкретному ИНН (физлицу) не существует ни в одном открытом реестре —
консулату нечего проверить.

Источник списка улиц: ручной отбор популярных/нейтральных улиц по каждому
региону. Избегаем:
- центральных улиц где много гос.учреждений (улица Мира, проспект Ленина)
  потому что любой проверяющий легко определит «странность»
- слишком длинных и трудных названий

ВАЖНО: дома 1-50 это реалистичный диапазон для большинства улиц.
Квартиры 1-150 — диапазон обычной 5-12 этажки.

ПЛАН: в Pack 17.4+ можно заменить на парсинг ФИАС/КЛАДР дамп если потребуется
больше регионов или большая вариативность. Пока этих 10 регионов достаточно
для нашего масштаба (~50 заявок/мес).
"""

