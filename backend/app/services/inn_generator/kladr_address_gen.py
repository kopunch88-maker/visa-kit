@dataclass(frozen=True)
class GeneratedAddress:
    """Сгенерированный почтовый адрес."""
    full: str          # "354000, Краснодарский край, г. Сочи, ул. Тепличная, д. 24, кв. 47"
    postal_code: str   # "354000"
    region_name: str   # "Краснодарский край"
    city_name: str     # "Сочи"
    street: str        # "ул. Тепличная"
    house: str         # "24"
    apartment: str     # "47"
    kladr_code: str    # KLADR региона


