def generate_address(
    kladr_code: str,
    rng: Optional[random.Random] = None,
) -> GeneratedAddress:
    """
    Генерирует случайный реалистичный адрес для региона по KLADR коду.

    Args:
        kladr_code: 13-значный KLADR код региона из таблицы KNOWN_REGIONS.
        rng: опциональный random.Random для воспроизводимости в тестах.

    Returns:
        GeneratedAddress с почтовым индексом, регионом, городом, улицей, домом, квартирой.

    Raises:
        ValueError: если регион не известен (kladr_code не в KNOWN_REGIONS).
    """
    if rng is None:
        rng = random.Random()

    if kladr_code not in KNOWN_REGIONS:
        raise ValueError(
            f"Region with kladr_code={kladr_code!r} not in KNOWN_REGIONS. "
            f"Available: {sorted(KNOWN_REGIONS.keys())}"
        )

    region = KNOWN_REGIONS[kladr_code]

    postal = rng.choice(region.postal_codes)
    street = rng.choice(region.streets)
    house = str(rng.randint(1, 80))
    apartment = str(rng.randint(1, 150))

    # Москва и СПб — города федерального значения, в адресе нет «область/край»
    if region.city_short.startswith("г. Москва") or region.city_short.startswith("г. Санкт-Петербург"):
        full = f"{postal}, {region.city_short}, {street}, д. {house}, кв. {apartment}"
        city_clean = region.city_short[len("г. "):]
    else:
        full = (
            f"{postal}, {region.region_full_name}, "
            f"{region.city_short}, {street}, д. {house}, кв. {apartment}"
        )
        city_clean = region.city_short[len("г. "):]

    log.debug(f"[address-gen] Generated: {full}")

    return GeneratedAddress(
        full=full,
        postal_code=postal,
        region_name=region.region_full_name,
        city_name=city_clean,
        street=street,
        house=house,
        apartment=apartment,
        kladr_code=kladr_code,
    )


def is_known_region(kladr_code: str) -> bool:
    """Возвращает True если для региона есть база улиц."""
    return kladr_code in KNOWN_REGIONS


def list_known_regions() -> List[str]:
    """Список KLADR кодов всех поддерживаемых регионов."""
    return sorted(KNOWN_REGIONS.keys())
