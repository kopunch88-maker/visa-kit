def run() -> None:
    log.warning("[Pack 33.3] starting PR legend_company seed migration...")

    with engine.begin() as conn:
        # 1. Найти id PR-специальности в БД (ожидаем 22 для code=42.03.01,
        #    но не хардкодим id — берём из БД через code).
        row = conn.execute(
            text("SELECT id FROM specialty WHERE code = :code"),
            {"code": "42.03.01"},
        ).first()
        if not row:
            raise RuntimeError(
                "[Pack 33.3] specialty code='42.03.01' (PR) НЕ найдена в БД. "
                "Сначала прогоните migration_pack19_0 чтобы залить специальности."
            )
        pr_specialty_id = row.id
        log.warning(
            "[Pack 33.3] resolved PR specialty: id=%s code='42.03.01' name=%s",
            pr_specialty_id, "Реклама и связи с общественностью",
        )

        # 2. Удаляем существующие PR-записи (для идемпотентности re-run).
        before_count = conn.execute(
            text(
                "SELECT COUNT(*) AS cnt FROM legend_company "
                "WHERE primary_specialty_id = :sid"
            ),
            {"sid": pr_specialty_id},
        ).scalar()
        log.warning(
            "[Pack 33.3] existing PR companies before migration: %d",
            before_count,
        )

        if before_count and before_count > 0:
            deleted = conn.execute(
                text(
                    "DELETE FROM legend_company "
                    "WHERE primary_specialty_id = :sid"
                ),
                {"sid": pr_specialty_id},
            )
            log.warning(
                "[Pack 33.3] deleted %d existing PR companies (re-run cleanup)",
                deleted.rowcount if hasattr(deleted, "rowcount") else "?",
            )

        # 3. Bulk INSERT новых PR-компаний.
        if not PR_LEGEND_COMPANIES_SEED:
            raise RuntimeError(
                "[Pack 33.3] PR_LEGEND_COMPANIES_SEED is empty — refuse to run"
            )

        values_sql = ", ".join(
            f"(:rc{i}, :ci{i}, :nf{i}, :ns{i}, :sid{i}, :sz{i}, TRUE)"
            for i in range(len(PR_LEGEND_COMPANIES_SEED))
        )
        params: dict = {}
        for i, (region_code, city, name_full, name_short, size) in enumerate(
            PR_LEGEND_COMPANIES_SEED
        ):
            params[f"rc{i}"] = region_code
            params[f"ci{i}"] = city
            params[f"nf{i}"] = name_full
            params[f"ns{i}"] = name_short
            params[f"sid{i}"] = pr_specialty_id
            params[f"sz{i}"] = size

        conn.execute(
            text(f"""
                INSERT INTO legend_company
                (region_code, city, name_full, name_short,
                 primary_specialty_id, size, is_active)
                VALUES {values_sql}
            """),
            params,
        )

        # 4. Финальная сводка
        after_count = conn.execute(
            text(
                "SELECT COUNT(*) AS cnt FROM legend_company "
                "WHERE primary_specialty_id = :sid"
            ),
            {"sid": pr_specialty_id},
        ).scalar()
        regions_count = conn.execute(
            text(
                "SELECT COUNT(DISTINCT region_code) AS cnt FROM legend_company "
                "WHERE primary_specialty_id = :sid"
            ),
            {"sid": pr_specialty_id},
        ).scalar()

        log.warning(
            "[Pack 33.3] FINAL: %d PR companies in %d regions (expected %d in 7)",
            after_count, regions_count, len(PR_LEGEND_COMPANIES_SEED),
        )

    log.warning("[Pack 33.3] migration complete")


if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)
    run()
