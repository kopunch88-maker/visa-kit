def run() -> None:
    log.warning("[Pack 33.5] starting LegendCompany seed migration...")
    log.warning("[Pack 33.5] %d LegendCompany records to seed", len(PACK_33_5_LEGEND_COMPANIES_SEED))

    with engine.begin() as conn:
        # 1. Resolve specialty codes -> ids
        codes = sorted({code for code, *_ in PACK_33_5_LEGEND_COMPANIES_SEED})
        rows = conn.execute(
            text("SELECT id, code FROM specialty WHERE code = ANY(:codes)"),
            {"codes": codes},
        ).all()
        specialty_id_map: dict[str, int] = {row.code: row.id for row in rows}

        missing = [c for c in codes if c not in specialty_id_map]
        if missing:
            raise RuntimeError(
                f"[Pack 33.5] specialty codes not found in DB: {missing}. "
                "Apply migration_pack19_0 first."
            )
        log.warning(
            "[Pack 33.5] resolved %d specialty codes to ids",
            len(specialty_id_map),
        )

        # 2. Idempotency: delete existing records under our 21 specialty_ids
        # БЕЗОПАСНО: для этих specialty_id сейчас 0 LegendCompany записей
        # (проверено диагностикой 10.05.2026).
        target_spec_ids = list(specialty_id_map.values())
        before_count = conn.execute(
            text(
                "SELECT COUNT(*) FROM legend_company "
                "WHERE primary_specialty_id = ANY(:sids)"
            ),
            {"sids": target_spec_ids},
        ).scalar()
        log.warning(
            "[Pack 33.5] existing LegendCompany records under target specialty_ids: %d",
            before_count,
        )

        if before_count and before_count > 0:
            deleted = conn.execute(
                text(
                    "DELETE FROM legend_company "
                    "WHERE primary_specialty_id = ANY(:sids)"
                ),
                {"sids": target_spec_ids},
            )
            log.warning(
                "[Pack 33.5] deleted %d existing records (re-run cleanup)",
                deleted.rowcount if hasattr(deleted, "rowcount") else "?",
            )

        # 3. Bulk INSERT — note legend_company has DEFAULT NOW() for timestamps
        # so no need to specify created_at/updated_at explicitly
        if not PACK_33_5_LEGEND_COMPANIES_SEED:
            raise RuntimeError("[Pack 33.5] PACK_33_5_LEGEND_COMPANIES_SEED is empty")

        values_sql = ", ".join(
            f"(:rc{i}, :ci{i}, :nf{i}, :ns{i}, :sid{i}, :sz{i}, TRUE)"
            for i in range(len(PACK_33_5_LEGEND_COMPANIES_SEED))
        )
        params: dict = {}
        for i, (spec_code, region_code, city, name_full, name_short, size) in enumerate(
            PACK_33_5_LEGEND_COMPANIES_SEED
        ):
            params[f"rc{i}"] = region_code
            params[f"ci{i}"] = city
            params[f"nf{i}"] = name_full
            params[f"ns{i}"] = name_short
            params[f"sid{i}"] = specialty_id_map[spec_code]
            params[f"sz{i}"] = size

        conn.execute(
            text(f"""
                INSERT INTO legend_company
                (region_code, city, name_full, name_short,
                 primary_specialty_id, size, is_active)
                VALUES {values_sql}
            """),
            params,
        )

        # 4. Final summary
        after_count = conn.execute(
            text(
                "SELECT COUNT(*) FROM legend_company "
                "WHERE primary_specialty_id = ANY(:sids)"
            ),
            {"sids": target_spec_ids},
        ).scalar()
        regions_count = conn.execute(
            text(
                "SELECT COUNT(DISTINCT region_code) FROM legend_company "
                "WHERE primary_specialty_id = ANY(:sids)"
            ),
            {"sids": target_spec_ids},
        ).scalar()

        log.warning(
            "[Pack 33.5] FINAL: %d LegendCompany records inserted across %d regions "
            "(expected %d in 4 regions)",
            after_count, regions_count, len(PACK_33_5_LEGEND_COMPANIES_SEED),
        )

    log.warning("[Pack 33.5] migration complete")


if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)
    run()
