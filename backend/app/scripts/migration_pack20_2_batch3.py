def upsert_position(conn, position_data: dict, specialty_id: int):
    existing = conn.execute(
        text(
            "SELECT id FROM position "
            "WHERE title_ru = :title_ru "
            "AND primary_specialty_id = :spec_id "
            "AND level = :level"
        ),
        {
            "title_ru": position_data["title_ru"],
            "spec_id": specialty_id,
            "level": position_data["level"],
        },
    ).fetchone()

    params = {
        "title_ru": position_data["title_ru"],
        "title_ru_genitive": position_data["title_ru_genitive"],
        "title_es": position_data["title_es"],
        "duties": json.dumps(position_data["duties"], ensure_ascii=False),
        "salary": position_data["salary_rub_default"],
        "tags": json.dumps(position_data["tags"], ensure_ascii=False),
        "profile": position_data["profile_description"],
        "spec_id": specialty_id,
        "level": position_data["level"],
    }

    if existing:
        position_id = existing[0]
        conn.execute(
            text("""
                UPDATE position SET
                    title_ru_genitive = :title_ru_genitive,
                    title_es = :title_es,
                    duties = CAST(:duties AS JSON),
                    salary_rub_default = :salary,
                    tags = CAST(:tags AS JSON),
                    profile_description = :profile,
                    is_active = TRUE,
                    updated_at = NOW()
                WHERE id = :pos_id
            """),
            {**params, "pos_id": position_id},
        )
        return position_id, "UPDATED"
    else:
        result = conn.execute(
            text("""
                INSERT INTO position (
                    title_ru, title_ru_genitive, title_es,
                    duties, salary_rub_default, tags,
                    profile_description, primary_specialty_id, level,
                    is_active, created_at, updated_at
                ) VALUES (
                    :title_ru, :title_ru_genitive, :title_es,
                    CAST(:duties AS JSON), :salary, CAST(:tags AS JSON),
                    :profile, :spec_id, :level,
                    TRUE, NOW(), NOW()
                )
                RETURNING id
            """),
            params,
        )
        return result.scalar(), "INSERTED"


def main():
    print("[Pack 20.2 batch 3] start: 12 Position для 3 специальностей")

    with engine.begin() as conn:
        total_inserted = 0
        total_updated = 0

        for specialty_code, positions in POSITIONS_BY_SPECIALTY.items():
            spec_row = conn.execute(
                text("SELECT id, name FROM specialty WHERE code = :code"),
                {"code": specialty_code},
            ).fetchone()
            if not spec_row:
                print(f"[Pack 20.2] ⚠️  SKIP specialty {specialty_code} — not found in DB")
                continue

            specialty_id, specialty_name = spec_row[0], spec_row[1]
            print(f"\n[Pack 20.2] === Specialty {specialty_code} '{specialty_name}' (id={specialty_id}) ===")

            for pos in positions:
                pos_id, action = upsert_position(conn, pos, specialty_id)
                level_name = {1: "Junior", 2: "Middle", 3: "Senior", 4: "Lead"}[pos["level"]]
                print(f"[Pack 20.2] {action} id={pos_id} ({level_name:<6}) "
                      f"{len(pos['duties']):>2} duties, {pos['salary_rub_default']:>7}₽  "
                      f"'{pos['title_ru']}'")
                if action == "INSERTED":
                    total_inserted += 1
                else:
                    total_updated += 1

        # Финальная картина
        print("\n[Pack 20.2] FINAL: все Position с заполненными specialty/level:")
        rows = conn.execute(text("""
            SELECT p.id, s.code, s.name, p.title_ru, p.level,
                   p.salary_rub_default,
                   jsonb_array_length(p.duties::jsonb) AS n_duties
            FROM position p
            JOIN specialty s ON p.primary_specialty_id = s.id
            ORDER BY s.code, p.level NULLS LAST, p.id
        """)).fetchall()
        current_spec = None
        for r in rows:
            if r[1] != current_spec:
                current_spec = r[1]
                print(f"\n  --- {r[1]} {r[2]} ---")
            level_label = {1: "Junior", 2: "Middle", 3: "Senior", 4: "Lead"}.get(r[4], "?")
            print(f"    id={r[0]:<3} L{r[4]} ({level_label:<6}) "
                  f"{r[5]:>10}₽  duties={r[6]:>2}  '{r[3]}'")

        print(f"\n[Pack 20.2 batch 3] ✅ DONE: inserted={total_inserted}, updated={total_updated}")


if __name__ == "__main__":
    main()
