# ============================================================================
# === Self-validation helper (опционально — для отладки seed) ===
# ============================================================================

def _self_check() -> None:
    """
    Локальная проверка целостности seed:
    - Все specialty_code в CAREER_TRACKS_SEED и LEGEND_COMPANIES_SEED должны
      существовать в universities_seed.SPECIALTIES_SEED.
    - Все region_code в LEGEND_COMPANIES_SEED должны быть валидными
      (2 цифры строкой).

    Запускать НЕ обязательно (миграция сама упадёт если specialty_code невалидный).
    """
    from app.seeds.universities_seed import SPECIALTIES_SEED
    valid_codes = {code for code, _, _ in SPECIALTIES_SEED}

    for code, level, title, _ in CAREER_TRACKS_SEED:
        assert code in valid_codes, f"CAREER_TRACKS: unknown specialty {code} for {title!r}"
        assert 1 <= level <= 4, f"CAREER_TRACKS: bad level {level} for {title!r}"

    for region_code, city, name_full, name_short, code, size in LEGEND_COMPANIES_SEED:
        assert code in valid_codes, f"LEGEND_COMPANIES: unknown specialty {code} for {name_short!r}"
        assert region_code.isdigit() and len(region_code) == 2, \
            f"LEGEND_COMPANIES: bad region_code {region_code!r} for {name_short!r}"
        assert size in ("small", "medium", "large"), \
            f"LEGEND_COMPANIES: bad size {size!r} for {name_short!r}"

    print(f"OK: {len(CAREER_TRACKS_SEED)} career tracks, "
          f"{len(LEGEND_COMPANIES_SEED)} legend companies")


if __name__ == "__main__":
    _self_check()
